							<li>Each team continues to roll until it beats the point of the opposite team. Only the "inside" team scores. One point is awarded to said team for each ball that is closer to the pallino than the "outside" team. The scoring team throws the pallino to begin the next frame. The player whose ball was closest to the pallino during the previous frame "pitches" the pallino, and then throws his or her ball. Order of flow must be continued throughout the frame. Regarding a single team, player A throws, then player B throws. This is repeated throughout the frame. Ex: A, B, A, B is correct. A, B, B, A is illegal. This continues until one team scores at least twelve points and has at least two points more than their opponents.</li>
							<li>While the object is to get close to the pallino, it is permissible for a player to roll his/her ball as to knock an opponent’s ball away from the pallino. Likewise, a player may knock or move the pallino toward his/her own team’s balls. The pallino is playable anywhere on the playing surface.</li>
							<li>A representative from each team is responsible for keeping a score sheet and reporting the results of each match.</li>
