							<li>The toss of a coin (or similar) determines which team will start. The starting team chooses which color (or pattern) ball they will play with. The first team member throws the pallino and then rolls his/her first ball as close to the pallino as possible. It is now up to the opposing team to roll a ball closer to the pallino than the starting team.</li>
