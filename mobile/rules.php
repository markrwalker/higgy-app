							<li>Any team that causes a minor infraction (throwing out of turn, throwing the opposing team's balls, crossing the line, etc) will relinquish the pitch to the opposing team upon the beginning of the next frame. If the wrong color ball is thrown, the field judge or a third party will replace it and the frame can continue.</li>
